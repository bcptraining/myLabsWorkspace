CREATE OR REPLACE TABLE EMPLOYEES_DIM_MERGE
(
EMPLOYEE_ID        NUMBER(6,0),
FIRST_NAME         VARCHAR(20),
LAST_NAME          VARCHAR(25),
EMAIL              VARCHAR(25),
PHONE_NUMBER       VARCHAR(20),
HIRE_DATE          DATE,
JOB_ID             VARCHAR(10),
SALARY             NUMBER(8,2),
COMMISSION_PCT     NUMBER(2,2),
MANAGER_ID         NUMBER(6,0),
DEPARTMENT_ID      NUMBER(4,0),
LAST_UPDATED_TS    TIMESTAMP ,
LAST_INSERTED_TS   TIMESTAMP 
);


CREATE OR REPLACE TASK TASK_UPDATE_EMPLOYEES_DIM_MERGE
WAREHOUSE = COMPUTE_WH 
SCHEDULE = '10 MINUTE'
AS
MERGE INTO SCHEDULE.EMPLOYEES_DIM_MERGE EDIM
USING HR.EMPLOYEES E
ON E.EMPLOYEE_ID = EDIM.EMPLOYEE_ID 
WHEN MATCHED THEN UPDATE
        SET 
        EDIM.EMPLOYEE_ID     = E.EMPLOYEE_ID,
        EDIM.FIRST_NAME      = E.FIRST_NAME,
        EDIM.LAST_NAME       = E.LAST_NAME,
        EDIM.EMAIL           = E.EMAIL,  
        EDIM.PHONE_NUMBER    = E.PHONE_NUMBER,
        EDIM.HIRE_DATE       = E.HIRE_DATE,
        EDIM.JOB_ID          = E.JOB_ID,  
        EDIM.SALARY          = E.SALARY,
        EDIM.COMMISSION_PCT  = E.COMMISSION_PCT,
        EDIM.MANAGER_ID      = E.MANAGER_ID, 
        EDIM.DEPARTMENT_ID   = E.DEPARTMENT_ID,
        EDIM.LAST_UPDATED_TS = CURRENT_TIMESTAMP()
WHEN NOT MATCHED THEN INSERT 
(EDIM.EMPLOYEE_ID,EDIM.FIRST_NAME, EDIM.LAST_NAME, EDIM.EMAIL, EDIM.PHONE_NUMBER,      
                                                                    EDIM.HIRE_DATE,EDIM.JOB_ID,EDIM.SALARY,EDIM.COMMISSION_PCT,EDIM.MANAGER_ID,EDIM.DEPARTMENT_ID,EDIM.LAST_UPDATED_TS,EDIM.LAST_INSERTED_TS)
VALUES
(E.EMPLOYEE_ID,E.FIRST_NAME, E.LAST_NAME, E.EMAIL, E.PHONE_NUMBER, E.HIRE_DATE,E.JOB_ID, E.SALARY,E.COMMISSION_PCT,E.MANAGER_ID,E.DEPARTMENT_ID,CURRENT_TIMESTAMP,CURRENT_TIMESTAMP);


ALTER TASK TASK_UPDATE_EMPLOYEES_DIM_MERGE  RESUME;


EXECUTE TASK TASK_UPDATE_EMPLOYEES_DIM_MERGE;


